package com.easypay.icard.entity;

public class AdvertiseInfo {

	private int id;
	private String titlePri;
	private String titleSub;
	private int businessId;
	/**
	 * @author lihbbruce
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @author lihbbruce
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @author lihbbruce
	 * @return the titlePri
	 */
	public String getTitlePri() {
		return titlePri;
	}
	/**
	 * @author lihbbruce
	 * @param titlePri the titlePri to set
	 */
	public void setTitlePri(String titlePri) {
		this.titlePri = titlePri;
	}
	/**
	 * @author lihbbruce
	 * @return the titleSub
	 */
	public String getTitleSub() {
		return titleSub;
	}
	/**
	 * @author lihbbruce
	 * @param titleSub the titleSub to set
	 */
	public void setTitleSub(String titleSub) {
		this.titleSub = titleSub;
	}
	/**
	 * @author lihbbruce
	 * @return the businessId
	 */
	public int getBusinessId() {
		return businessId;
	}
	/**
	 * @author lihbbruce
	 * @param businessId the businessId to set
	 */
	public void setBusinessId(int businessId) {
		this.businessId = businessId;
	}
}
